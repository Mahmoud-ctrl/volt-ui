export const getIcon = (iconName: string, size: number = 16) => {
  const IconComponent = iconRegistry[iconName];
  if (!IconComponent) {
    console.warn(`Icon "${iconName}" not found in registry`);
    return null;
  }
  return <IconComponent size={size} />;
};